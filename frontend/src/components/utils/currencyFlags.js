export function getFlagEmojiCharCode(countryCode) {
  return countryCode.toUpperCase().replace(/./g, (char) =>
    String.fromCodePoint(127397 + char.charCodeAt()),
  );
}

export function getFlagEmoji(currencyCode) {
  const countryCode = currency2CountryCodes[currencyCode];
  return countryCode? getFlagEmojiCharCode(countryCode): '';
}

export function countryFlags() {
  return (
    Object.keys(currency2CountryCodes).map(
        (item) => {
          return ([item + ' ' + getFlagEmoji(item)]);
        },
    )
  );
}
